import { Solver } from './solver';

describe('Day 25: Code Chronicle', () => {
  it('should parse input', () => {
    const solver = new Solver();
    const parsed = solver.parseInput(EXAMPLE);
    expect(parsed.keys.length).toEqual(3);
    expect(parsed.keys[0]).toEqual([5, 0, 2, 1, 3]);
    expect(parsed.keys[1]).toEqual([4, 3, 4, 0, 2]);
    expect(parsed.keys[2]).toEqual([3, 0, 2, 0, 1]);
    expect(parsed.locks.length).toEqual(2);
    expect(parsed.locks[0]).toEqual([0, 5, 3, 4, 3]);
    expect(parsed.locks[1]).toEqual([1, 2, 0, 5, 3]);
  });

  it('should solve part 1 for example data', () => {
    const solver = new Solver();
    const { locks, keys } = solver.parseInput(EXAMPLE);
    expect(solver.solvePart1(locks, keys)).toEqual(3);
  });
  it('should solve part 1 for full data', () => {
    const solver = new Solver();
    const { locks, keys } = solver.parseInput(FULL_DATA);
    expect(solver.solvePart1(locks, keys)).toEqual(2854);
  });


});

